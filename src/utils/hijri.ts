  const reminders: HijriReminder[] = [];
  const day = hijri.day;
  const month = hijri.month;
  const dayOfWeek = gregorianDate.getDay(); // 0 is Sunday, 1 is Monday, etc.

  // 1. Month-level key virtues
  if (month === 1) { // Muharram
    reminders.push({
      type: 'gold',
      title: "أول شهور السنة الهجرية",
      description: "نستقبل شهراً من أشهر الله الحرم (المحرّم)، يستحب فيه الإكثار من الصيام وصالح الأعمال.",
      badge: "الأشهر الحرم"
    });
  } else if (month === 7) { // Rajab
    reminders.push({
      type: 'gold',
      title: "أقبل شهر رجب الحرام",
      description: "بوابة غرس الطاعات تمهيداً لرمضان الصيام والقيام، عظم رغبتك بالتقرب والإنابة.",
      badge: "الأشهر الحرم"
    });
  } else if (month === 8) { // Shaban
    reminders.push({
      type: 'info',
      title: "شعبان رفع الأعمال",
      description: "تنال فيه الأعمال العرض الأكبر على رب الأكوان، عوّد نفسك الطاعة تمهيداً لرمضان المبارك.",
      badge: "شعبان"
    });
  } else if (month === 9) { // Ramadan
    reminders.push({
      type: 'success',
      title: "شهر رمضان المبارك",
      description: "تُفتح فيه أبواب الجنان وتُغلق أبواب النيران. تزوّد بالقرآن والقيام والأعمال الصالحة.",
      badge: "رمضان الكريم"
    });
  } else if (month === 11) { // Dhul Qadah
    reminders.push({
      type: 'gold',
      title: "دخل شهر ذي القعدة الحرام",
      description: "من الأشهر الحرم المتتالية، يغتنم بالطاعات وعظيم تجنب الذنوب والآثام.",
      badge: "الأشهر الحرم"
    });
  } else if (month === 12) { // Dhul Hijjah
    reminders.push({
      type: 'gold',
      title: "موسم الهدی والعمل العظيم",
      description: "سيد الأشهر في الفضيلة، يستعد فيه المسلمون لأعظم الأيام تقربًا وإنابة.",
      badge: "موسم الحج"
    });
  }

