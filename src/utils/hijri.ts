/**
 * Modern, dependency-free wrapper for getting current Hijri coordinates via Intl API
 */
export function getCurrentHijriDate(offsetDays: number = 0): HijriDate {
  try {
    const today = new Date();
    if (offsetDays !== 0) {
      today.setDate(today.getDate() + offsetDays);
    }
    
    // Formatting via Intl.DateTimeFormat Um Al-Qura
    const formatterEn = new Intl.DateTimeFormat('en-TN-u-ca-islamic-umalqura', {
      day: 'numeric',
      month: 'numeric',
      year: 'numeric'
    });
    
    const parts = formatterEn.formatToParts(today);
    let hDay = today.getDate(); // Fallbacks
    let hMonth = 1;
    let hYear = 1447;
    
    for (const part of parts) {
      if (part.type === 'day') hDay = parseInt(part.value, 10);
      if (part.type === 'month') hMonth = parseInt(part.value, 10);
      if (part.type === 'year') hYear = parseInt(part.value, 10);
    }
    
    // Safety boundaries for array access
    if (hMonth < 1 || hMonth > 12) hMonth = 1;
    
    const monthNameAr = HIJRI_MONTHS_AR[hMonth - 1];
    
    // Convert to elegant Arabic text format
    const formattedAr = `${hDay} ${monthNameAr} ${hYear} هـ`;
    
    return {
      day: hDay,
      month: hMonth,
      year: hYear,
      monthNameAr,
      formattedAr
    };
  } catch (error) {
    console.warn("Intl Hijri calendar format error, using basic mockup fallback", error);
    return {
      day: 27,
      month: 12,
      year: 1447,
      monthNameAr: "ذو الحجة",
      formattedAr: "27 ذو الحجة 1447 هـ"
    };
  }
}

export interface HijriReminder {
  type: 'success' | 'warning' | 'info' | 'gold';
  title: string;
  description: string;
  badge: string;
}

/**
 * Calculates notifications/reminders based on current Hijri Date & Day of the week
 */
